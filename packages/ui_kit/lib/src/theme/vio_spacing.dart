/// Vio Spacing System
class VioSpacing {
  VioSpacing._();

  // ============== Base Spacing Scale ==============
  /// 2px - minimal spacing
  static const double xxs = 2;

  /// 4px - tight spacing
  static const double xs = 4;

  /// 8px - small spacing
  static const double sm = 8;

  /// 12px - medium-small spacing
  static const double md = 12;

  /// 16px - medium spacing (base unit)
  static const double lg = 16;

  /// 20px - medium-large spacing
  static const double xl = 20;

  /// 24px - large spacing
  static const double xxl = 24;

  /// 32px - extra large spacing
  static const double xxxl = 32;

  /// 48px - huge spacing
  static const double huge = 48;

  // ============== Component Specific ==============
  /// Padding inside buttons
  static const double buttonPaddingH = 16;
  static const double buttonPaddingV = 8;

  /// Icon button size
  static const double iconButtonSize = 32;
  static const double iconButtonSizeSmall = 24;
  static const double iconButtonSizeLarge = 40;

  /// Input field padding
  static const double inputPaddingH = 12;
  static const double inputPaddingV = 8;

  /// Panel padding
  static const double panelPadding = 12;

  /// Toolbar height
  static const double toolbarHeight = 48;

  /// Sidebar width
  static const double sidebarWidth = 240;
  static const double sidebarWidthCollapsed = 48;

  /// Properties panel width
  static const double propertiesPanelWidth = 280;

  // ============== Border Radius ==============
  /// No radius
  static const double radiusNone = 0;

  /// Small radius - subtle rounding
  static const double radiusSm = 4;

  /// Medium radius - default for most components
  static const double radiusMd = 6;

  /// Large radius - cards, panels
  static const double radiusLg = 8;

  /// Extra large radius - modals, large cards
  static const double radiusXl = 12;

  /// Full radius - pills, circular
  static const double radiusFull = 9999;

  // ============== Border Width ==============
  /// Default border
  static const double borderWidth = 1;

  /// Thick border (focus states)
  static const double borderWidthThick = 2;

  // ============== Icon Sizes ==============
  /// Small icon
  static const double iconSm = 16;

  /// Default icon
  static const double iconMd = 20;

  /// Large icon
  static const double iconLg = 24;

  /// Extra large icon
  static const double iconXl = 32;

  // ============== Shadows ==============
  /// Elevation levels (for box shadows)
  static const double elevation1 = 1;
  static const double elevation2 = 2;
  static const double elevation3 = 4;
  static const double elevation4 = 8;
  static const double elevation5 = 16;
}
