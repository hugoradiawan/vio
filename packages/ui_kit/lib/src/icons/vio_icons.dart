import 'package:flutter/material.dart';

/// Icon definitions for Vio design system.
/// Uses Material icons.
class VioIcons {
  VioIcons._();

  // Transform icons
  static const IconData rotation = Icons.rotate_right;
  static const IconData cornerRadius = Icons.rounded_corner;
  static const IconData sizeHorizontal = Icons.swap_horiz;
  static const IconData sizeVertical = Icons.swap_vert;

  // Effects icons
  static const IconData dropShadow = Icons.filter_drama_outlined;
  static const IconData innerShadow = Icons.filter_drama;

  // Stroke icons
  static const IconData strokeSize = Icons.line_weight;

  // Alignment icons
  static const IconData alignLeft = Icons.align_horizontal_left;
  static const IconData alignHorizontalCenter =
      Icons.align_horizontal_center;
  static const IconData alignRight = Icons.align_horizontal_right;
  static const IconData alignTop = Icons.align_vertical_top;
  static const IconData alignVerticalCenter =
      Icons.align_vertical_center;
  static const IconData alignBottom = Icons.align_vertical_bottom;

  // Flip icons
  static const IconData flipHorizontal = Icons.flip;
  static const IconData flipVertical = Icons.swap_vert;

  // Lock icons
  static const IconData lock = Icons.lock_outlined;
  static const IconData unlock = Icons.lock_open_outlined;

  // Visibility icons
  static const IconData eye = Icons.visibility_outlined;
  static const IconData eyeOff = Icons.visibility_off_outlined;

  // Action icons
  static const IconData add = Icons.add;
  static const IconData remove = Icons.remove;
  static const IconData close = Icons.close;

  // Fill and stroke
  static const IconData fill = Icons.format_color_fill;
  static const IconData stroke = Icons.border_color;
}
